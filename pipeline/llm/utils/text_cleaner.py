"""
한국어 구어체 필러 제거 유틸리티 (regex 기반)

LLM 전처리 대비 속도: ~0ms vs ~12s
커버 범위: 알려진 패턴만 처리. 미등록 표현은 통과.
재학습 후: preprocess_node를 다시 LLM 방식으로 교체 가능.
"""

import re

# ── 문장 첫머리 단독 추임새 ───────────────────────────────────────────────────
# 이전: 뒤에 공백만 처리 → "음..." "어..." 통과 버그
# 수정: 공백 또는 구두점·줄임표·물결 등 뒤에 오는 경우 모두 처리
_LEADING_INTERJECTION = re.compile(
    r'^(?:아+|어+|음+|으+|에+|예+|네+)[\s,.·…~\-]+'
)

# ── 문장 첫머리 전환 표현 ─────────────────────────────────────────────────────
# "자, 이제" / "인제" / "자 그럼" 같은 방송 진행 전환어
_LEADING_TRANSITION = re.compile(
    r'^(?:자+[,，\s]+(?:이제\s+)?|인제\s+|자\s*그럼\s*)'
)

# ── 구어체 전환·메타 표현 ─────────────────────────────────────────────────────
_FILLER_PHRASES = re.compile(
    r'(?:'
    r'그니까|그러니까'              # 그니까 / 그러니까
    r'|어\s*그러니까'              # 어 그러니까
    r'|제\s*말은'                  # 제 말은
    r'|뭐랄까요?'                  # 뭐랄까 / 뭐랄까요
    r'|있잖아요?'                  # 있잖아 / 있잖아요
    r'|어떻게\s*말하면'            # 어떻게 말하면
    r'|그게\s*뭐냐면'              # 그게 뭐냐면
    r'|솔직히\s*말하면'            # 솔직히 말하면
    r'|어떻게\s*보면'              # 어떻게 보면 (hedge)
    r'|말하자면'                   # 말하자면
    r'|쉽게\s*말해서'              # 쉽게 말해서
    r'|간단히\s*말하면'            # 간단히 말하면
    r'|뭐\s*그냥'                  # 뭐 그냥
    r'|그냥\s*뭐'                  # 그냥 뭐
    r'|아\s*뭐'                   # 아 뭐 (전환)
    r'|어\s*뭐'                   # 어 뭐 (전환)
    r')\s*[,，]?\s*'
)

# ── 말더듬 (같은 한국어 단어 연속 반복) ──────────────────────────────────────
# "그 그", "이 이", "그래서 그래서" 등
_STUTTER = re.compile(r'([가-힣]{1,5})\s+\1(?=\s|[,，.。!?]|$)')

# ── 문장 끝 군더더기 ─────────────────────────────────────────────────────────
# "...뭐", "...뭐 이런 거", "...아무튼" 등 의미 없는 마무리
_TRAILING_FILLER = re.compile(
    r'[,，\s]+'
    r'(?:뭐\s*이런\s*(?:거|것)|아무튼\s*뭐?|뭐)$'
)

# ── 공백 정리 ────────────────────────────────────────────────────────────────
_MULTI_SPACE = re.compile(r'\s+')


def clean_fillers(text: str) -> str:
    """
    구어체 필러를 제거하고 정제된 텍스트를 반환한다.
    원문 의미에 영향을 주지 않는 표현만 제거한다.
    """
    text = _LEADING_INTERJECTION.sub('', text)
    text = _LEADING_TRANSITION.sub('', text)
    text = _FILLER_PHRASES.sub('', text)
    text = _STUTTER.sub(r'\1', text)
    text = _TRAILING_FILLER.sub('', text)
    text = _MULTI_SPACE.sub(' ', text).strip()
    return text


# ── 단위 테스트 ───────────────────────────────────────────────────────────────
if __name__ == "__main__":
    cases = [
        ("음...",                           ""),
        ("어...",                           ""),
        ("아, 그래서 MVP가 중요해요.",        "그래서 MVP가 중요해요."),
        ("네, 오늘 주제는 번아웃이에요.",     "오늘 주제는 번아웃이에요."),
        ("그니까 핵심은 범위를 줄이는 거예요.", "핵심은 범위를 줄이는 거예요."),
        ("어 그러니까, 집중 블록이 중요해요.", "집중 블록이 중요해요."),
        ("뭐랄까, 번아웃은 서서히 와요.",    "번아웃은 서서히 와요."),
        ("어떻게 보면 그게 강점이에요.",      "그게 강점이에요."),
        ("그 그 다음 단계로 넘어가면",        "그 다음 단계로 넘어가면"),
        ("열심히 하고 있어요, 뭐",           "열심히 하고 있어요"),
        ("자, 이제 질문 받겠습니다.",         "질문 받겠습니다."),
        ("인제 정리해드릴게요.",              "정리해드릴게요."),
    ]

    print(f"{'입력':<40}  →  {'출력'}")
    print("-" * 70)
    ok = 0
    for src, expected in cases:
        result = clean_fillers(src)
        status = "OK" if result == expected else "FAIL"
        if result == expected:
            ok += 1
        print(f"{status}  {src!r:<40}  ->  {result!r}")
        if result != expected:
            print(f"   expected: {expected!r}")
    print(f"\n{ok}/{len(cases)} 통과")
